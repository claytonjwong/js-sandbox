/**
 * @param {number[]} A
 * @return {number}
 */
var maxNumberOfApples = function(A, cap=5000, ans=0) {
    A.sort((a, b) => {
        return a - b;
    });
    for (let weight of A) {
        if (cap - weight >= 0) {
            cap -= weight;
            ++ans;
        }
    }
    return ans;
};

